          }
        ]
      },
      salarArr: [3000, 5000, 8000, 10000, 15000, 20000, 25000, 30000, 40000, 50000, 60000, 70000, 80000, 90000, 99999],
      developer: '李志升 duosanglee@gmail.com',
      // select框焦点
      selectActive: ''
    }
  },
  computed: {
    selectorApi () {
      // 因为有些功能未完成 先把那些功能提供的数据写上
      let result = {
        sorting: '0',
        area: ''
      }
      return Object.keys(this.obj).reduce((itemA, key) => {
        let filterActive = this.obj[key].filter(filterVal => {
          return filterVal['active']
        })[0]['apiData']
        itemA[key] = String(filterActive)
        return itemA
      }, result)
    },
    // 所选最少月薪
    selectMinWageType () {
      return this.obj['minWage'].filter(val => {
        return val.active
      })[0]['name']
    },
    // 所选最多月薪
    selectMaxWageType () {
      return this.obj['maxWage'].filter(val => {
        return val.active
      })[0]['name']
    }
    // 所选工作性质
    // selectPropertyType () {
    //   return this.obj['property'].filter(val => {
    //     return val.active
    //   })[0]['name']
    // }
  },
  methods: {
    // 点击select框
    setSelectActive (type) {
      this.selectActive = type
    },
    compareWage (key) {
      let obj = this.obj
      let ActiveMinWage = obj['minWage'].filter(filterVal => {
        return filterVal['active']
      })[0]
      let ActiveMaxWage = obj['maxWage'].filter(filterVal => {
        return filterVal['active']
      })[0]
      if (ActiveMinWage['apiData'] * ActiveMaxWage['apiData'] !== 0) {
        if (key === 'minWage') {
          if (ActiveMinWage['apiData'] > ActiveMaxWage['apiData']) {
            this.activeChange(obj['maxWage'], obj['maxWage'][0], 'maxWage')
          }
        } else if (key === 'maxWage') {
          if (ActiveMaxWage['apiData'] < ActiveMinWage['apiData']) {
            this.activeChange(obj['minWage'], obj['minWage'][0], 'minWage')
          }
        }
      }
    },
    // 焦点变化
    activeChange (itemArr, item, key) {
      // item 所点的列表数据
      // itemArr item所属的对象
      // key 所点击的属性

      itemArr.forEach((val, index, arr) => {
        this.$set(val, 'active', false)
      }, this)
      this.$set(item, 'active', true)

      this.$store.commit('updateSearchCondition',
        {
          key,
          value: String(item['apiData'])
        }
      )

      this.compareWage(key)

      this.$store.dispatch('queryJobList', this)
      // 取消select框焦点
      this.selectActive = ''
    }
  },
  destroyed () {
    // 销毁
    Object.keys(this.selectorApi).forEach(val => {
      this.$store.commit('removeSearchCondition',
        {
          key: val
        }
      )
    })
  },
  created () {
    // 工资数据初始化
    this.salarArr.forEach((val, index, arr) => {
      let number = Math.round(val / 1000) + 'k'
      this.obj.minWage.push({
        apiData: val,
        name: number
      })
      this.obj.maxWage.push({
        apiData: val,
        name: number
      })
    }, this)

    // 注册
    Object.keys(this.selectorApi).forEach(val => {
      this.$store.commit('addSearchCondition',
        {
          key: val,
          value: String(this.selectorApi[val])
        }
      )
    })
    /** 2017/07/24  零零水
     *  移除这里的列表拉取（不应该放在这里）
     */
  }
}
